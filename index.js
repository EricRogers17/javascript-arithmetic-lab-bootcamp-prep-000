function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function inc(n) {
  var num = n
  num += n;
  return num;
}

function dec(n) {
  var num = n;
  num -= n;
  return num;
}

function makeInt(a) {
  return parseInt('a', 10);
}

function perserveDecimal(a) {
  return parseFloat('a');
}
